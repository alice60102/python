for a in range(2,10):
    for b in range(1,10):
        print(a,"*",b,"=",a*b,end="\t")

for a in range(1,11):
    for b in range(1,a):
        print(b,end="")
    else:
        print()


for a in range(1,11):
    for b in range(1,10-a):
        print(b,end="")
    else:
        print()

